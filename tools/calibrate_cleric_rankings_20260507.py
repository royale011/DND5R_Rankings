# -*- coding: utf-8 -*-
from pathlib import Path
import re

ROOT = Path(__file__).resolve().parents[1]
CLERIC = ROOT / "Rankings" / "牧师"
TIERS = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]


def rows(vals, reasons):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    out += [f"| {tier} | {rank} | {reason} |" for tier, rank, reason in zip(TIERS, vals, reasons)]
    return "\n".join(out)


def update_file(name, vals, reasons, calibration):
    path = CLERIC / name
    text = path.read_text(encoding="utf-8")
    text = re.sub(
        r"## 综合评分\s*\| 阶段 \| 综合强度 \| 具体理由 \|\n\|---\|---\|---\|\n(?:\| Tier [^\n]+\n){4}",
        "## 综合评分\n" + rows(vals, reasons) + "\n",
        text,
    )
    marker = "## 分项评分"
    if marker in text:
        head, tail = text.split(marker, 1)
        head = re.sub(r"\n## 本轮重评\n.*?\n(?=\n## |\Z)", "\n", head, flags=re.S)
        head = re.sub(r"\n本轮校准：.*?\n\n\Z", "\n", head, flags=re.S)
        head = head.rstrip() + "\n\n## 本轮重评\n" + calibration.strip() + "\n\n"
        text = head + marker + tail
    text = re.sub(
        r"## 校准说明\n.*?(?=\n\Z|\Z)",
        "## 校准说明\n本轮重评先核对`DND5e_chm`本地牧师、领域、2024职业底盘、TCE/旧版/UA/合作方文本，再参考社区对2024牧师、和平领域、暮光领域、生命/光明/诡术/战争领域与`神圣干预`施法时间争议的讨论。最终评分按当前项目标准处理：`综合评分`是成品牧师强度，必须同时说明牧师底盘、领域增量与限制器；`神圣干预`动作压缩只在保留目标、距离、持续、触发、位置、专注和桌裁限制后计入。",
        text,
        flags=re.S,
    )
    path.write_text(text, encoding="utf-8")


