def merged(**overrides):
    out = {k: v[:] for k, v in BASE.items()}
    out.update(overrides)
    return out


def aspect_table(scores):
    lines = [
        "## 分项评分",
        "| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |",
        "|---|---|---|---|---|",
    ]
    for key in ASPECTS:
        lines.append(f"| {key} | {' | '.join(scores.get(key, BASE[key]))} |")
    return "\n".join(lines)


def overall_table(rows):
    lines = ["## 综合评分", "| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIERS, rows):
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


def design_table(rows):
    lines = ["## 设计相关评分", "| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for item, rank, reason in rows:
        lines.append(f"| {item} | {rank} | {reason} |")
    return "\n".join(lines)


def write_file(filename, title, summary, detail, aspects, overall, design):
    text = "\n\n".join(
        [
            f"# {title}",
            "## 队伍定位摘要",
            summary.strip(),
            "## 详细评价",
            detail.strip(),
            aspect_table(aspects),
            overall_table(overall),
            design_table(design),
            "## 校准说明",
            CALIBRATION,
            "",
        ]
    )
    (CLASS_DIR / filename).write_text(text, encoding="utf-8", newline="\n")


