def replace_all(path: Path, replacements: list[tuple[str, str]]) -> bool:
    text = path.read_text(encoding="utf-8")
    old_text = text
    for old, new in replacements:
        text = text.replace(old, new)
    if text != old_text:
        path.write_text(text, encoding="utf-8")
        return True
    return False


def append_record():
    note = (
        "\n\n## 2026-05-05 Cleric 5e-to-5.5e compatibility calibration\n"
        "- Updated official skill: old 5e/2014 subclass features or subclass spells granted below level 3 are treated as level-3 gains on the 2024/5.5e chassis.\n"
        "- Updated official skill: for 5e/2014 Cleric subclasses, Heavy Armor, Martial Weapon, Divine Strike, and Potent Spellcasting grants are ignored for balance rankings because 2024 Cleric `神圣秩序` and `受祝击` cover or overwrite those chassis benefits.\n"
        "- Revisited affected Cleric review wording and reasons. No major overall rank changes were needed beyond the previous Forge Domain Tier 4 calibration; affected files now avoid crediting old heavy armor / martial weapon / Divine Strike timing as subclass power.\n"
    )
    text = RECORD.read_text(encoding="utf-8") if RECORD.exists() else ""
    if "2026-05-05 Cleric 5e-to-5.5e compatibility calibration" not in text:
        RECORD.write_text(text.rstrip() + note + "\n", encoding="utf-8")


def main():
    changed = []
    for filename, replacements in REPLACEMENTS.items():
        path = CLERIC / filename
        if path.exists() and replace_all(path, replacements):
            changed.append(filename)
    append_record()
    print("\n".join(changed))


if __name__ == "__main__":
    main()
