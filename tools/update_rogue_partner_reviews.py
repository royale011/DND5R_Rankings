def write(path, text):
    path.write_text(text, encoding="utf-8")


def rename_if_exists(old, new):
    old_path = ROGUE / old
    new_path = ROGUE / new
    if old_path.exists() and old_path != new_path:
        if new_path.exists():
            old_path.unlink()
        else:
            old_path.rename(new_path)


def extract_overall(path):
    rows, in_sec = [], False
    for line in path.read_text(encoding="utf-8").splitlines():
        if line.strip() == "## 综合评分":
            in_sec = True
            continue
        if in_sec and line.startswith("## "):
            break
        if in_sec and line.startswith("| Tier"):
            parts = [p.strip() for p in line.strip("|").split("|")]
            if len(parts) >= 3:
                rows.append((parts[0], parts[1], parts[2]))
    return rows


def render_leaderboard(title, intro, names):
    order = ["S+", "S", "S-", "A+", "A", "A-", "B+", "B", "C", "D", "E", "E-", "F"]
    data = {tier: {} for tier in TIERS}
    for name in names:
        path = ROGUE / f"{name}.md"
        if not path.exists():
            continue
        for tier, rank, reason in extract_overall(path):
            data.setdefault(tier, {}).setdefault(rank, []).append((name, reason))
    out = [f"## {title}", ""]
    if intro:
        out.extend([intro, ""])
    for tier in TIERS:
        out.append(f"{tier}:")
        any_rank = False
        for rank in order:
            entries = data.get(tier, {}).get(rank, [])
            if not entries:
                continue
            any_rank = True
            out.append(f"- {rank}:")
            for name, reason in entries:
                out.append(f"  - {name}：{reason}")
        if not any_rank:
            out.append("- 无")
        out.append("")
    return "\n".join(out).rstrip()


