def score_table(scores):
    lines = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for cat, vals in zip(CATEGORIES, scores):
        lines.append(f"| {cat} | {' | '.join(vals)} |")
    return "\n".join(lines)


def overall_table(overalls):
    lines = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIERS, overalls):
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


def design_table(items):
    lines = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for item, rank, reason in items:
        lines.append(f"| {item} | {rank} | {reason} |")
    return "\n".join(lines)


