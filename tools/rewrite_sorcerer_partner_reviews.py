def update_summary_leaderboards() -> None:
    summary = OUT / "README.md"
    official = {}
    non_first = {}
    for path in sorted(OUT.glob("*.md"), key=lambda p: p.name):
        if path.name == "README.md":
            continue
        non = "（" in path.stem and "）" in path.stem
        try:
            rows = extract_overall(path)
        except Exception:
            continue
        if non:
            non_first[path.stem] = rows
        else:
            official[path.stem] = rows
    replacement = render_leaderboard("分阶段子职排行榜", official)
    if non_first:
        replacement += "\n\n" + render_leaderboard("UA/合作方/第三方子职分阶段排行榜", non_first)
    replacement += "\n"
    text = summary.read_text(encoding="utf-8")
    start = text.find("## 分阶段子职排行榜")
    if start < 0:
        text = text.rstrip() + "\n\n" + replacement
    else:
        text = text[:start].rstrip() + "\n\n" + replacement
    summary.write_text(text, encoding="utf-8")


