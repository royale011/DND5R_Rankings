def split_row(line: str) -> list[str]:
    line = line.strip()
    if not (line.startswith("|") and line.endswith("|")):
        return []
    return [part.strip() for part in line.strip("|").split("|")]


def extract_overall(path: Path) -> list[tuple[str, str]]:
    text = path.read_text(encoding="utf-8")
    section = text.split("## 综合评分", 1)[1]
    nxt = re.search(r"\n## ", section)
    if nxt:
        section = section[:nxt.start()]
    rows = []
    for line in section.splitlines():
        parts = split_row(line)
        if len(parts) >= 3 and parts[0] in TIERS:
            rows.append((parts[1], parts[2]))
    if len(rows) != 4:
        raise ValueError(f"{path} has invalid overall table")
    return rows


def render_leaderboard(title: str, entries: dict[str, list[tuple[str, str]]]) -> str:
    out = [f"## {title}", ""]
    for idx, tier in enumerate(TIERS):
        out.append(f"{tier}:")
        buckets: dict[str, list[tuple[str, str]]] = {}
        for name, rows in entries.items():
            buckets.setdefault(rows[idx][0], []).append((name, rows[idx][1]))
        for rank in RANK_ORDER:
            if rank not in buckets:
                continue
            out.append(f"- {rank}:")
            for name, reason in buckets[rank]:
                out.append(f"  - {name}：{reason}")
        out.append("")
    return "\n".join(out).rstrip()


