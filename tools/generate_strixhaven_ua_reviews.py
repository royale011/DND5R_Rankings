def table_scores(subclass: str, cls: str) -> str:
    ranks = RANKS[(cls, subclass)]
    reasons = [
        f"{CLASS_NOTES[cls]}3级取得斯翠海文1+包，低级强度主要看初始法术/特性是否立刻改变动作经济。",
        f"6级节点让{subclass}进入完整中阶循环；此阶段评分同时看基础职业5-10级施法/资源窗口。",
        f"Tier 3按本职业实际子职槽处理。若本职业延迟或跳过10+/14+特性，评分已计入这个损失。",
        f"终局强度主要由基础职业高阶底盘和已取得的斯翠海文特性共同决定；未把UA身份本身作为降档理由。",
    ]
    lines = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, rank, reason in zip(["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"], ranks, reasons):
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


