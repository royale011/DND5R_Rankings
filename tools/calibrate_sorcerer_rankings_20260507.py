def clone_scores(source: dict[str, list[str]], **updates: list[str]) -> dict[str, list[str]]:
    data = {key: list(value) for key, value in source.items()}
    for key, value in updates.items():
        data[key] = list(value)
    return data


def overall_table(vals: list[str], reasons: list[str]) -> str:
    rows = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, val, reason in zip(TIERS, vals, reasons):
        rows.append(f"| {tier} | {val} | {reason} |")
    return "\n".join(rows)


def design_table(vals: dict[str, tuple[str, str]]) -> str:
    rows = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for key in ["设计质量", "主题", "能力设计质量", "主题与能力关联度"]:
        rank, reason = vals[key]
        rows.append(f"| {key} | {rank} | {reason} |")
    return "\n".join(rows)


