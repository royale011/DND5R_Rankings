def apply_override(name: str, source: dict[str, object]) -> dict[str, object]:
    data = deepcopy(source)
    patch = OVERRIDES[name]
    data["overall"] = patch["overall"]
    data["overall_reasons"] = patch["reasons"]
    scores = clone_scores(data["scores"], **patch.get("scores", {}))
    data["scores"] = scores
    extra = patch.get("calibration", "")
    if extra and extra not in data["details"]:
        data["details"] = data["details"].rstrip() + "\n\n本轮校准：" + extra
    return data


def make_base_review() -> str:
    rows = [(aspect, BASE_ASPECTS[aspect]) for aspect in ASPECTS]
    aspect_table = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for name, vals in rows:
        aspect_table.append("| " + name + " | " + " | ".join(vals) + " |")
    notes = "\n".join(f"- **{key}**：{value}" for key, value in BASE_NOTES.items())
    return f"""# 术士（Sorcerer）总评

## 队伍定位摘要
术士是爆发型奥术满施法者。2024版通过`先天术法`、更多准备法术、短休恢复术法点和更清晰的超魔循环，把它从窄法术表职业提升为可靠的战斗控制/爆发核心；但它仍不是法师式全域工具箱。

## 详细评价
{BASE_DETAILS}

## 分项评分
{chr(10).join(aspect_table)}

## 分项理由
{notes}

## 综合评分
{overall_table(BASE_OVERALL, BASE_REASONS)}

## 设计相关评分
{design_table(BASE_DESIGN)}

## 校准说明
本轮依据`DND5e_chm\\玩家手册2024\\角色职业\\术士`与现有术士子职本地文本重新校准。核心变化是采用满施法者压缩标准：术士本职高等级默认 S-，只有子职实际补足扩表、反制、隐蔽施法、专注保护、团队防护或战场规则改写时才进入 S。
"""


def write_review(name: str, source: dict[str, object], calibration: str) -> None:
    data = apply_override(name, source)
    text = make_review(
        name,
        name,
        data["intro"],
        data["details"],
        data["scores"],
        data["notes"],
        data["overall"],
        data["overall_reasons"],
        data["design"],
        calibration,
    )
    (OUT / f"{name}.md").write_text(text, encoding="utf-8")


