from pathlib import Path


ROOT = Path(".")


def replace_section(text: str, heading: str, body: str) -> str:
    marker = f"## {heading}"
    start = text.index(marker)
    next_start = text.find("\n## ", start + len(marker))
    if next_start == -1:
        next_start = len(text)
    return text[:start] + marker + "\n" + body.strip() + "\n\n" + text[next_start:].lstrip("\n")


def write_file(path: Path, sections: dict[str, str]) -> None:
    text = path.read_text(encoding="utf-8")
    for heading, body in sections.items():
        text = replace_section(text, heading, body)
    path.write_text(text, encoding="utf-8")


def replace_row(path: Path, item: str, vals: tuple[str, str, str, str]) -> None:
    text = path.read_text(encoding="utf-8")
    lines = text.splitlines()
    target = f"| {item} |"
    for idx, line in enumerate(lines):
        if line.startswith(target):
            lines[idx] = f"| {item} | {vals[0]} | {vals[1]} | {vals[2]} | {vals[3]} |"
            path.write_text("\n".join(lines) + "\n", encoding="utf-8")
            return
    raise ValueError(f"row not found: {path} {item}")


def design(rows: list[tuple[str, str, str]]) -> str:
    lines = [
        "| 设计项 | 评分 | 具体理由 |",
        "|---|---|---|",
    ]
    for item, score, reason in rows:
        lines.append(f"| {item} | {score} | {reason} |")
    return "\n".join(lines)


