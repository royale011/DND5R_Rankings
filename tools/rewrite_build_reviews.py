def table(rows):
    out = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for k, v in rows.items():
        out.append(f"| {k} | {' | '.join(v)} |")
    return "\n".join(out)


def overall(rows):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, rank, reason in rows:
        out.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(out)


