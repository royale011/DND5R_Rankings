def update_summary():
    summary = ROOT / "Rankings" / "README.md"
    if not summary.exists():
        return
    text = summary.read_text(encoding="utf-8")
    marker = "## 构筑综合强度表"
    if marker not in text:
        return
    before, rest = text.split(marker, 1)
    after_marker = "\n## "
    if after_marker in rest:
        old, after = rest.split(after_marker, 1)
        suffix = after_marker + after
    else:
        suffix = ""
    rows = [marker, "| 构筑 | Tier 1 | Tier 2 | Tier 3 | Tier 4 |", "|---|---|---|---|---|"]
    for filename, data in BUILDS.items():
        ranks = [r for _, r, _ in data["overall"]]
        rows.append(f"| {data['title']} | {' | '.join(ranks)} |")
    summary.write_text(before.rstrip() + "\n\n" + "\n".join(rows) + "\n" + suffix, encoding="utf-8")


