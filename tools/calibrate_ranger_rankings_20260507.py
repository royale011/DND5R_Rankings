def md_table(rows):
    out = [
        "| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |",
        "|---|---|---|---|---|",
    ]
    for key, vals in rows:
        out.append(f"| {key} | {' | '.join(vals)} |")
    return "\n".join(out)


def overall_table(vals, reasons):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, val, reason in zip(TIERS, vals, reasons):
        out.append(f"| {tier} | {val} | {reason} |")
    return "\n".join(out)


def design_table(design):
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for key in ["设计质量", "主题", "能力设计质量", "主题与能力关联度"]:
        out.append(f"| {key} | {design[key][0]} | {design[key][1]} |")
    return "\n".join(out)


def review(title, intro, details, sc, notes, overall, reasons, design, calibration):
    note_text = "\n".join(f"- **{key}**：{value}" for key, value in notes.items())
    return f"""# {title}

## 队伍定位摘要
{intro}

## 详细评价
{details}

## 分项评分
{md_table((aspect, sc[aspect]) for aspect in ASPECTS)}

## 分项理由
{note_text}

## 综合评分
{overall_table(overall, reasons)}

## 设计相关评分
{design_table(design)}

## 校准说明
{calibration}
"""


def design(q, theme, feature, align):
    return {
        "设计质量": q,
        "主题": theme,
        "能力设计质量": feature,
        "主题与能力关联度": align,
    }


