        path = DIR / filename
        text = path.read_text(encoding="utf-8")
        text = replace_section(text, data["position_title"], data["position"])  # type: ignore[arg-type]
        text = replace_section(text, "详细评价", data["detail"] + "\n\n" + COMMON_NOTE)  # type: ignore[operator]
        if "design" in data:
            text = replace_section(text, "设计相关评分", design_table(data["design"]))  # type: ignore[arg-type]
        for row, values in data.get("rows", {}).items():  # type: ignore[union-attr]
            text = replace_row(text, row, values)
        path.write_text(text, encoding="utf-8", newline="\n")


