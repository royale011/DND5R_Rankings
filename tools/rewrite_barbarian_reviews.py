def replace_section(text: str, title: str, body: str) -> str:
    pattern = re.compile(rf"(^## {re.escape(title)}\n)(.*?)(?=^## |\Z)", re.S | re.M)
    if not pattern.search(text):
        raise RuntimeError(f"missing section {title}")
    return pattern.sub(lambda m: m.group(1) + body.strip() + "\n\n", text, count=1)


def replace_row(text: str, row: str, values: list[str]) -> str:
    pattern = re.compile(rf"^\| {re.escape(row)} \|.*$", re.M)
    return pattern.sub(f"| {row} | {' | '.join(values)} |", text, count=1)


def design_table(rows: list[tuple[str, str, str]]) -> str:
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for item, score, reason in rows:
        out.append(f"| {item} | {score} | {reason} |")
    return "\n".join(out)


