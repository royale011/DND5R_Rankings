def apply_overrides() -> dict[str, dict[str, object]]:
    data = deepcopy(FILES)
    for filename, patch in OVERRIDES.items():
        target = data[filename]
        if "review_append" in patch:
            target["review"] = target["review"].rstrip() + patch["review_append"]
        if "overall" in patch:
            target["overall"] = patch["overall"]
        if "scores" in patch:
            target["scores"] = patch["scores"]
        if "design" in patch:
            target["design"] = patch["design"]
    return data


def prepend_changelog() -> None:
    path = ROOT / "Rankings" / "changelog.md"
    current = path.read_text(encoding="utf-8") if path.exists() else ""
    marker = "灵能使满施法者压缩重评"
    if marker in current:
        return
    stamp = datetime.now().strftime("%Y-%m-%d")
    entry = f"""## {stamp} 灵能使满施法者压缩重评

- 依据最新满施法者压缩标准重评UA职业`灵能使`及其当前子职。基础职业、`蜕变者`、`念动使`、`传心师`使用`其他\\新UA\\灵能二期`；`裂空使`因二期未替换，继续使用一期`其他\\新UA\\灵能`文本。
- UA身份不自动降低强度；但正式出版前的文本不稳定性、缺少`祈愿术`、法表生态窄于法师、以及社区讨论仍未充分沉淀，都写入`综合评分`理由。
- 灵能使本职高等级从默认 S 校准为 S-：它是强满施法者，但默认不拥有法师式法术书/仪式经济、日备全表或`祈愿术`生态。
- 子职校准：`念动使（UA）`因硬控制、防御和无专注`心灵遥控`保留 S；`裂空使（UA）`因群体空间重排在Tier 3-4为 S；`传心师（UA）`和`蜕变者（UA）`按强满施法底盘加较窄子职增量校准到 S- / A+ 区间。

"""
    path.write_text(entry + current.lstrip(), encoding="utf-8")


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    data = apply_overrides()
    for filename, item in data.items():
        write_review(OUT / filename, item)
    prepend_changelog()


if __name__ == "__main__":
    main()