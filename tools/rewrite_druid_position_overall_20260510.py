    text = rename_position_heading(text, "队伍定位摘要", heading)
    text = rename_position_heading(text, "队伍定位总结", heading)
    text = rename_position_heading(text, "队伍定位调整", heading)
    text = replace_section(text, heading, position)
    text = replace_section(text, "详细评价", detail)
    text = replace_section(text, "综合评分", overall(rows))
    write_utf8_lf(path, text)


