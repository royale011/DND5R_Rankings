def write_utf8_lf(path: Path, text: str) -> None:
    path.write_bytes((text.rstrip() + "\n").encode("utf-8"))


def replace_section(text: str, heading: str, body: str) -> str:
    pattern = re.compile(rf"## {re.escape(heading)}\n.*?(?=\n## |\Z)", re.S)
    replacement = f"## {heading}\n{body.rstrip()}\n"
    if not pattern.search(text):
        raise ValueError(f"missing section: {heading}")
    return pattern.sub(replacement, text, count=1)


def rename_position_heading(text: str, old: str, new: str) -> str:
    text = text.replace(f"## {old}\n", f"## {new}\n", 1)
    return text


def overall(rows: list[tuple[str, str, str]]) -> str:
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, rank, reason in rows:
        out.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(out)


