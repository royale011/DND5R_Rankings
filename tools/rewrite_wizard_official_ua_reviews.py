def md_table(rows):
    out = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for k, vals in rows:
        out.append(f"| {k} | {' | '.join(vals)} |")
    return "\n".join(out)


def overall_table(vals, reasons):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for t, v, r in zip(TIERS, vals, reasons):
        out.append(f"| {t} | {v} | {r} |")
    return "\n".join(out)


def design_table(design):
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for k in ["设计质量", "主题", "能力设计质量", "主题与能力关联度"]:
        out.append(f"| {k} | {design[k][0]} | {design[k][1]} |")
    return "\n".join(out)


def review(title, intro, details, sc, notes, overall, reasons, design, calibration):
    note_text = "\n".join(f"- **{k}**：{v}" for k, v in notes.items())
