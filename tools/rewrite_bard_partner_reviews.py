def table(rows):
    out = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for name, vals in rows:
        out.append(f"| {name} | {' | '.join(vals)} |")
    return "\n".join(out)


def overall(rows):
    out = ["## 综合评分", "| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, rank, reason in rows:
        out.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(out)


def design(rows):
    out = ["## 设计相关评分", "| 项目 | 评分 | 具体理由 |", "|---|---|---|"]
    for item, rank, reason in rows:
        out.append(f"| {item} | {rank} | {reason} |")
    return "\n".join(out)


