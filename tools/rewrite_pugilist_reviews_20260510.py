def write_utf8_lf(path: Path, text: str) -> None:
    path.write_bytes((text.rstrip() + "\n").encode("utf-8"))


def replace_section(text: str, heading: str, body: str) -> str:
    pattern = rf"(^## {re.escape(heading)}\n)(.*?)(?=^## |\Z)"
    replacement = "\\g<1>" + body.rstrip() + "\n\n"
    new_text, count = re.subn(pattern, replacement, text, count=1, flags=re.M | re.S)
    if count != 1:
        raise ValueError(f"missing section: {heading}")
    return new_text


def table(rows: list[tuple[str, str, str]]) -> str:
    lines = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, rank, reason in rows:
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


def design_table(rows: list[tuple[str, str, str]]) -> str:
    lines = ["| 项目 | 评分 | 具体理由 |", "|---|---|---|"]
    for item, rank, reason in rows:
        lines.append(f"| {item} | {rank} | {reason} |")
    return "\n".join(lines)


