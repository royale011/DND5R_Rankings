def aspect_table(scores: dict[str, list[str]]) -> str:
    lines = [
        "## 分项评分",
        "| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |",
        "|---|---|---|---|---|",
    ]
    for aspect in ASPECTS:
        lines.append(f"| {aspect} | {' | '.join(scores.get(aspect, BASE_ASPECTS[aspect]))} |")
    return "\n".join(lines)


def overall_table(rows: list[tuple[str, str]]) -> str:
    lines = ["## 综合评分", "| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIERS, rows):
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


def design_table(rows: list[tuple[str, str, str]]) -> str:
    lines = ["## 设计相关评分", "| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    lines.extend(f"| {item} | {rank} | {reason} |" for item, rank, reason in rows)
    return "\n".join(lines)


def merged(overrides: dict[str, list[str]] | None = None) -> dict[str, list[str]]:
    scores = {key: value[:] for key, value in BASE_ASPECTS.items()}
    if overrides:
        scores.update(overrides)
    return scores


def write_review(
    filename: str,
    title: str,
    summary: str,
    detail: str,
    aspects: dict[str, list[str]],
    overall: list[tuple[str, str]],
    design: list[tuple[str, str, str]],
    source: str | None = None,
    english: str | None = None,
    adjustment_heading: str = "队伍定位调整",
) -> None:
    meta = []
    if filename != "README.md":
        meta.append("所属职业：游荡者")
        if english:
            meta.append(f"英文名 / 社区检索名：{english}")
        if source:
            meta.append(f"来源 / 版本：{source}")
    blocks = [f"# {title}"]
    if meta:
        blocks.append("\n".join(meta))
    blocks.extend([
        f"## {adjustment_heading}",
        summary.strip(),
        "## 详细评价",
        detail.strip(),
        aspect_table(aspects),
        overall_table(overall),
        design_table(design),
        "## 校准说明",
        COMMON_CALIBRATION,
    ])
    (CLASS_DIR / filename).write_text("\n\n".join(blocks).rstrip() + "\n", encoding="utf-8", newline="\n")


