from pathlib import Path
import re

ROOT = Path(__file__).resolve().parents[1]
OUT = ROOT / "Rankings" / "魔契师"
CHANGELOG = ROOT / "Rankings" / "changelog.md"

TIER_LABELS = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]


def overall_table(rows: list[tuple[str, str]]) -> str:
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIER_LABELS, rows):
        out.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(out)


def replace_section(text: str, heading: str, body: str) -> str:
    pattern = rf"({re.escape(heading)}\n)(.*?)(?=\n## |\Z)"
    return re.sub(pattern, lambda m: m.group(1) + body.rstrip() + "\n", text, flags=re.S)


def update_review(name: str, detail_addendum: str, rows: list[tuple[str, str]]) -> None:
    path = OUT / f"{name}.md"
    text = path.read_text(encoding="utf-8")

    detail = re.search(r"## 详细评价\n(.*?)(?=\n## 分项评分)", text, re.S)
    if detail:
        old_detail = detail.group(1).rstrip()
        marker = "\n\n本轮魔契师压缩校准："
        if marker in old_detail:
            old_detail = old_detail.split(marker, 1)[0].rstrip()
        text = replace_section(text, "## 详细评价", old_detail + marker + detail_addendum)

    text = replace_section(text, "## 综合评分", overall_table(rows))
    path.write_text(text, encoding="utf-8")


