def extract_overall(text):
    rows = {}
    in_table = False
    for line in text.splitlines():
        if line == "## 综合评分":
            in_table = True
            continue
        if in_table and line.startswith("## "):
            break
        if in_table and line.startswith("| Tier"):
            parts = [p.strip() for p in line.strip("|").split("|")]
            rows[parts[0]] = (parts[1], parts[2])
    return rows


def leaderboard(title, data):
    rank_order = ["S+", "S", "S-", "A+", "A", "A-", "B+", "B", "C", "D", "E", "E-", "F"]
    out = [f"## {title}", ""]
    if title.startswith("UA/"):
        out.append("本节只列入 UA / 合作方 / 第三方战士子职。它们按实际功能评分，不因来源降分；其中 `回音骑士（EGW）` 因项目例外也列入官方榜，但仍在本节作为合作方条目重复列出。")
        out.append("")
    for tier in TIERS:
        out.append(f"{tier}:")
        buckets = {}
        for name, rows in data.items():
            rank, reason = rows[tier]
            buckets.setdefault(rank, []).append((name, reason))
        for rank in rank_order:
            if rank not in buckets:
                continue
            out.append(f"- {rank}:")
            for name, reason in buckets[rank]:
                out.append(f"  - {name}：{reason}")
        out.append("")
    return "\n".join(out).rstrip() + "\n"


