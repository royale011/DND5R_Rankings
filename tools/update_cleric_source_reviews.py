def table(items):
    rows = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    rows.extend(f"| {name} | {a} | {b} | {c} | {d} |" for name, a, b, c, d in items)
    return "\n".join(rows)


