def overall(rows):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    labels = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]
    for label, (rank, reason) in zip(labels, rows):
        out.append(f"| {label} | {rank} | {reason} |")
    return "\n".join(out)


def design(rows):
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for name, rank, reason in rows:
        out.append(f"| {name} | {rank} | {reason} |")
    return "\n".join(out)


