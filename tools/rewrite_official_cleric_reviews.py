TIER_LABELS = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]


def aspect_table(rows=ASPECT_ROWS):
    out = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    out.extend(f"| {a} | {b} | {c} | {d} | {e} |" for a, b, c, d, e in rows)
    return "\n".join(out)


def overall_table(rows):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for label, (rank, reason) in zip(TIER_LABELS, rows):
        out.append(f"| {label} | {rank} | {reason} |")
    return "\n".join(out)


def design_table(rows):
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    out.extend(f"| {name} | {rank} | {reason} |" for name, rank, reason in rows)
    return "\n".join(out)


