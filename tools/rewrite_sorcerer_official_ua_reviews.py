def table(rows):
    s = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for name, vals in rows:
        s.append("| " + name + " | " + " | ".join(vals) + " |")
    return "\n".join(s)


def overall_table(vals, reasons):
    s = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, val, reason in zip(TIERS, vals, reasons):
        s.append(f"| {tier} | {val} | {reason} |")
    return "\n".join(s)


def design_table(vals):
    s = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for k in ["设计质量", "主题", "能力设计质量", "主题与能力关联度"]:
        rank, reason = vals[k]
        s.append(f"| {k} | {rank} | {reason} |")
    return "\n".join(s)


def make_review(title, subtitle, intro, details, aspect_scores, aspect_notes, overall, overall_reasons, design, calibration):
    rows = [(a, aspect_scores[a]) for a in ASPECTS]
    notes = "\n".join(f"- **{k}**：{v}" for k, v in aspect_notes.items())
    return f"""# {title}

## 队伍定位摘要
{intro}

## 详细评价
{details}

## 分项评分
{table(rows)}

## 分项理由
{notes}

## 综合评分
{overall_table(overall, overall_reasons)}

## 设计相关评分
{design_table(design)}

## 校准说明
{calibration}
"""


def lb_section(title, entries):
    out = [f"## {title}"]
    if not entries:
        out.append("本轮没有纳入该类别的子职。")
        return "\n".join(out)
    for i, tier in enumerate(TIERS):
        out.append(f"{tier}:")
        by_rank = {}
        for name, data in entries.items():
            r = data["overall"][i]
            by_rank.setdefault(r, []).append((name, data["overall_reasons"][i]))
        for rank in RANK_ORDER:
            if rank not in by_rank:
                continue
            out.append(f"- {rank}:")
            for name, reason in by_rank[rank]:
                out.append(f"  - {name}: {reason}")
    return "\n".join(out)


