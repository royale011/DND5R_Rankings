def main() -> None:
    existing = {p.name for p in BUILD_DIR.glob("*.md")}
    missing = sorted(set(DOCS) - existing)
    extra = sorted(existing - set(DOCS))
    if missing:
        raise SystemExit(f"Missing build files: {missing}")
    if extra:
        raise SystemExit(f"Unexpected build files: {extra}")

    for name, text in DOCS.items():
        write_utf8_lf(BUILD_DIR / name, text)
    prepend_changelog()


if __name__ == "__main__":
    main()
