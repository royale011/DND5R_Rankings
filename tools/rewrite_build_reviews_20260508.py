def table(rows: list[tuple[str, str, str, str, str]]) -> str:
    out = [
        "| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |",
        "|---|---|---|---|---|",
    ]
    out += [f"| {name} | {t1} | {t2} | {t3} | {t4} |" for name, t1, t2, t3, t4 in rows]
    return "\n".join(out)


def overall(rows: list[tuple[str, str]]) -> str:
    labels = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]
    out = [
        "| 阶段 | 综合强度 | 具体理由 |",
        "|---|---|---|",
    ]
    out += [f"| {label} | {rank} | {reason} |" for label, (rank, reason) in zip(labels, rows)]
    return "\n".join(out)


def doc(
    title: str,
    english: str,
    link: str,
    details: str,
    role: str,
    review: str,
    scores: list[tuple[str, str, str, str, str]],
    overall_rows: list[tuple[str, str]],
    sanity: str,
) -> str:
    return dedent(
        f"""\
        # {title}

        英文/社区称呼：{english}
        社区来源链接：{link}

        ## 构筑细节
        {details.strip()}

        ## 队伍定位
        {role.strip()}

        ## 详细评价
        {review.strip()}

        ## 分项评分
        {table(scores)}

        ## 综合评分
        {overall(overall_rows)}

        ## 真实强度复核
        {sanity.strip()}

        ## 校准结论
        {COMMON_NOTE}
        """
    ).replace("\n        ", "\n").strip() + "\n"


