def update_theurgy_readme() -> None:
    readme = OUT / "README.md"
    text = readme.read_text(encoding="utf-8")
    start = text.find("## 分阶段子职排行榜")
    if start < 0:
        raise RuntimeError("神圣奇术 README missing leaderboard marker")
    replacement = render_leaderboard("分阶段子职排行榜", row_entries(DOMAINS))
    replacement += "\n\n## UA/合作方/第三方子职分阶段排行榜\n\n本轮只生成官方牧师领域映射；UA、合作方、第三方牧师领域映射待后续评审。\n"
    intro = text[:start].rstrip()
    # Keep the credited compatible text, then regenerate the base review and leaderboards.
    base_marker = "## 队伍定位摘要"
    if base_marker in intro:
        intro = intro[:intro.find(base_marker)].rstrip()
    write(readme, intro + "\n\n" + theurgy_base_review().rstrip() + "\n\n" + replacement)


