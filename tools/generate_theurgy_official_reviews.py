def render_leaderboard(title: str, entries: dict[str, list[tuple[str, str]]]) -> str:
    out = [f"## {title}"]
    for idx, tier in enumerate(TIERS):
        out.extend(["", f"### {tier}", "", "| 阶段 | 评级 | 子职 | 具体理由 |", "|---|---|---|---|"])
        rows = []
        for name, scores in entries.items():
            rank, reason = scores[idx]
            rows.append((rank, name, reason))
        rows.sort(key=lambda item: (RANK_ORDER.index(item[0]), item[1]))
        for rank, name, reason in rows:
            out.append(f"| {TIER_SHORT[idx]} | {rank} | {name} | {reason} |")
    return "\n".join(out)


