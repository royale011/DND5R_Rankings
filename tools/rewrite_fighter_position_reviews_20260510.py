TIERS = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]


def write(path: Path, text: str) -> None:
    path.write_text(text.rstrip() + "\n", encoding="utf-8", newline="\n")


def replace_section(text: str, heading: str, body: str) -> str:
    pattern = re.compile(rf"^## {re.escape(heading)}\n.*?(?=^## |\Z)", re.M | re.S)
    repl = f"## {heading}\n\n{body.strip()}\n\n"
    new, count = pattern.subn(repl, text, count=1)
    if count != 1:
        raise ValueError(f"missing section: {heading}")
    return new


def replace_position(text: str, heading: str, body: str) -> str:
    pattern = re.compile(r"^## 队伍定位(?:摘要|总结|调整)\n.*?(?=^## |\Z)", re.M | re.S)
    repl = f"## {heading}\n\n{body.strip()}\n\n"
    new, count = pattern.subn(repl, text, count=1)
    if count != 1:
        raise ValueError("missing position section")
    return new


def replace_overall_reasons(text: str, rows: list[tuple[str, str]]) -> str:
    def repl(match: re.Match[str]) -> str:
        section = match.group(0)
        lines = section.splitlines()
        out = []
        tier_i = 0
        for line in lines:
            parts = [p.strip() for p in line.strip().strip("|").split("|")]
            if len(parts) >= 3 and parts[0] in TIERS:
                expected_tier = TIERS[tier_i]
                if parts[0] != expected_tier:
                    raise ValueError(f"unexpected tier row: {parts[0]} != {expected_tier}")
                expected_rank, reason = rows[tier_i]
                if parts[1] != expected_rank:
                    raise ValueError(f"rank drift in {parts[0]}: {parts[1]} != {expected_rank}")
                out.append(f"| {parts[0]} | {parts[1]} | {reason} |")
                tier_i += 1
            else:
                out.append(line)
        if tier_i != 4:
            raise ValueError(f"expected 4 overall rows, got {tier_i}")
        return "\n".join(out)

    pattern = re.compile(r"^## 综合评分\n.*?(?=^## |\Z)", re.M | re.S)
    new, count = pattern.subn(repl, text, count=1)
    if count != 1:
        raise ValueError("missing overall section")
    return new


