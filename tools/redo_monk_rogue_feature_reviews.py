from pathlib import Path


def replace_section(text: str, heading: str, body: str) -> str:
    marker = f"## {heading}"
    start = text.index(marker)
    nxt = text.find("\n## ", start + len(marker))
    if nxt == -1:
        return text[:start] + marker + "\n" + body.rstrip() + "\n"
    return text[:start] + marker + "\n" + body.rstrip() + "\n" + text[nxt:]


def replace_row(text: str, item: str, vals: list[str]) -> str:
    lines = text.splitlines()
    prefix = f"| {item} |"
    row = f"| {item} | " + " | ".join(vals) + " |"
    return "\n".join(row if line.startswith(prefix) else line for line in lines) + ("\n" if text.endswith("\n") else "")


def design(rows):
    return "\n".join(["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"] + [f"| {a} | {b} | {c} |" for a, b, c in rows])


def apply(root: Path, name: str, cfg: dict):
    p = root / name
    text = p.read_text(encoding="utf-8")
    if "summary" in cfg:
        text = replace_section(text, cfg.get("summary_heading", "队伍定位调整"), cfg["summary"])
    text = replace_section(text, "详细评价", cfg["review"])
    if "design" in cfg:
        text = replace_section(text, "设计相关评分", design(cfg["design"]))
    for item, vals in cfg.get("rows", {}).items():
        text = replace_row(text, item, vals)
    p.write_text(text, encoding="utf-8")


