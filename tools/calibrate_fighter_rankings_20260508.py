def aspect_table(scores: dict[str, list[str]]) -> str:
    lines = [
        "## 分项评分",
        "| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |",
        "|---|---|---|---|---|",
    ]
    for aspect in ASPECTS:
        vals = scores.get(aspect, BASE_ASPECTS[aspect])
        lines.append(f"| {aspect} | {' | '.join(vals)} |")
    return "\n".join(lines)


def overall_table(rows: list[tuple[str, str]]) -> str:
    lines = ["## 综合评分", "| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIERS, rows):
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


def design_table(rows: list[tuple[str, str, str]]) -> str:
    lines = ["## 设计相关评分", "| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for item, rank, reason in rows:
        lines.append(f"| {item} | {rank} | {reason} |")
    return "\n".join(lines)


def merged(**overrides: list[str]) -> dict[str, list[str]]:
    scores = {k: v[:] for k, v in BASE_ASPECTS.items()}
    scores.update(overrides)
    return scores


def merged_extra(overrides: dict[str, list[str]]) -> dict[str, list[str]]:
    scores = {k: v[:] for k, v in BASE_ASPECTS.items()}
    scores.update(overrides)
    return scores


def write_review(
    filename: str,
    title: str,
    summary: str,
    detail: str,
    aspects: dict[str, list[str]],
    overall: list[tuple[str, str]],
    design: list[tuple[str, str, str]],
    calibration: str = COMMON_CALIBRATION,
) -> None:
    text = "\n\n".join(
        [
            f"# {title}",
            "## 队伍定位摘要",
            summary.strip(),
            "## 详细评价",
            detail.strip(),
            aspect_table(aspects),
            overall_table(overall),
            design_table(design),
            "## 校准说明",
            calibration.strip(),
            "",
        ]
    )
    (CLASS_DIR / filename).write_text(text, encoding="utf-8", newline="\n")


