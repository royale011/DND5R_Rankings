TIER_LABELS = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]
ASPECTS = [
    "近战伤害者", "远程伤害者", "法术伤害者", "防御和生存", "治疗和临时生命", "团队增益",
    "反制施法者", "减益", "战场分隔", "召唤 / 伙伴", "技能使用", "制造",
]


def aspect_table(scores: dict[str, list[str]]) -> str:
    rows = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for a in ASPECTS:
        vals = scores[a]
        rows.append(f"| {a} | {vals[0]} | {vals[1]} | {vals[2]} | {vals[3]} |")
    return "\n".join(rows)


def overall_table(rows: list[tuple[str, str]]) -> str:
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIER_LABELS, rows):
        out.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(out)


def design_table(rows: list[tuple[str, str]]) -> str:
    names = ["设计质量", "主题", "能力设计质量", "主题与能力关联度"]
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for name, (rank, reason) in zip(names, rows):
        out.append(f"| {name} | {rank} | {reason} |")
    return "\n".join(out)


