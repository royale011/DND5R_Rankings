from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
RANGER = ROOT / "Rankings" / "游侠"


def rename_if_needed(old: str, new: str) -> None:
    old_path = RANGER / old
    new_path = RANGER / new
    if old_path.exists() and not new_path.exists():
        old_path.rename(new_path)


def write(name: str, text: str) -> None:
    RANGER.mkdir(parents=True, exist_ok=True)
    (RANGER / name).write_text(text.strip() + "\n", encoding="utf-8")


def table(rows):
    out = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for row in rows:
        out.append("| " + " | ".join(row) + " |")
    return "\n".join(out)


