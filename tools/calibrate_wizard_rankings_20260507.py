# -*- coding: utf-8 -*-
from pathlib import Path
import re

ROOT = Path(__file__).resolve().parents[1]
WIZ = ROOT / "Rankings" / "法师"
TIERS = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]


def rows(vals, reasons):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    out += [f"| {t} | {v} | {r} |" for t, v, r in zip(TIERS, vals, reasons)]
    return "\n".join(out)


def update_file(name, vals, reasons, calibration):
    path = WIZ / name
    text = path.read_text(encoding="utf-8")
    text = re.sub(
        r"## 综合评分\s*\| 阶段 \| 综合强度 \| 具体理由 \|\n\|---\|---\|---\|\n(?:\| Tier [^\n]+\n){4}",
        "## 综合评分\n" + rows(vals, reasons) + "\n",
        text,
    )
    if "## 分项评分" in text:
        head, tail = text.split("## 分项评分", 1)
        head = re.sub(r"\n本轮校准：.*?\n\n\Z", "\n", head, flags=re.S)
        head = head.rstrip() + "\n\n" + calibration + "\n\n"
        text = head + "## 分项评分" + tail
    text = re.sub(
        r"## 校准说明\n.*?(?=\n\Z|\Z)",
        "## 校准说明\n本轮重评先核对`DND5e_chm`本地法师/子职文本，再参考社区对2024法师、预言/防护/幻术、剑咏、战争魔法、书士会、EGW时间/重力魔法等讨论。最终评分按当前项目标准处理：`综合评分`是成品角色强度，必须同时说明法师底盘、子职增量与限制器；不会把强法师底盘机械等同为所有子职S，也不会把弱子职按纯模块低估。",
        text,
        flags=re.S,
    )
    path.write_text(text, encoding="utf-8")


