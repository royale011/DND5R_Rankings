def table_aspects(rows):
    lines = [
        "| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |",
        "|---|---|---|---|---|",
    ]
    for aspect in ASPECTS:
        vals = rows[aspect]
        lines.append(f"| {aspect} | {vals[0]} | {vals[1]} | {vals[2]} | {vals[3]} |")
    return "\n".join(lines)


def table_overall(rows):
    lines = [
        "| 阶段 | 综合强度 | 具体理由 |",
        "|---|---|---|",
    ]
    for tier, rank, reason in rows:
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


def doc(title, english, sources, details, role, review, aspects, overall, sanity, conclusion):
    source_block = "\n".join(f"- {s}" for s in sources)
    parts = [
        f"# {title}",
        "",
        f"英文/社区称呼：{english}",
        "",
        "社区参考：",
        source_block,
        "",
        TOC,
        "## 构筑细节",
        "\n\n".join(details),
        "",
        "## 队伍定位",
        "\n\n".join(role),
        "",
        "## 详细评价",
        "\n\n".join(review),
        "",
        "## 分项评分",
        table_aspects(aspects),
        "",
        "## 综合评分",
        table_overall(overall),
        "",
        "## 真实强度复核",
        "\n\n".join(sanity),
        "",
        "## 校准结论",
        "\n\n".join(conclusion),
        "",
    ]
    return "\n".join(parts)


