from __future__ import annotations

import pathlib
import re


ROOT = pathlib.Path("Rankings")


def replace_design_row(path: pathlib.Path, item: str, reason: str, rank: str | None = None) -> bool:
    text = path.read_text(encoding="utf-8")
    pattern = re.compile(rf"^\| {re.escape(item)} \| ([^|]+) \| .* \|$", re.M)

    def repl(match: re.Match[str]) -> str:
        next_rank = rank if rank is not None else match.group(1).strip()
        return f"| {item} | {next_rank} | {reason} |"

    new_text, count = pattern.subn(repl, text, count=1)
    if count:
        path.write_text(new_text, encoding="utf-8", newline="")
        return True
    return False


def update_rows(mapping: dict[str, dict[str, tuple[str | None, str]]]) -> int:
    changed = 0
    for rel, rows in mapping.items():
        path = ROOT / rel
        if not path.exists():
            raise FileNotFoundError(path)
        for item, (rank, reason) in rows.items():
            if replace_design_row(path, item, reason, rank):
                changed += 1
    return changed


