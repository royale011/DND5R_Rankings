def table(scores):
    rows = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for a in ASPECTS:
        v = scores[a]
        rows.append(f"| {a} | {v[0]} | {v[1]} | {v[2]} | {v[3]} |")
    return "\n".join(rows)


def overall(rows):
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIER_LABELS, rows):
        out.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(out)


def design(rows):
    keys = ["设计质量", "主题", "能力设计质量", "主题与能力关联度"]
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for key, (rank, reason) in zip(keys, rows):
        out.append(f"| {key} | {rank} | {reason} |")
    return "\n".join(out)


