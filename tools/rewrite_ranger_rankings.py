def aspect_table(vals):
    lines = ["| 项目 | Tier 1（1-4） | Tier 2（5-10） | Tier 3（11-16） | Tier 4（17-20） |", "|---|---|---|---|---|"]
    for aspect in ASPECTS:
        lines.append("| " + aspect + " | " + " | ".join(vals[aspect]) + " |")
    return "\n".join(lines)


def overall_table(vals):
    lines = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, rank, reason in vals:
        lines.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(lines)


def design_table(vals):
    lines = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    for item, rank, reason in vals:
        lines.append(f"| {item} | {rank} | {reason} |")
    return "\n".join(lines)


