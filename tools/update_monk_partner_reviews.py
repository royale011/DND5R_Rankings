def write(path, text):
    path.write_text(text, encoding="utf-8")


def rename_if_exists(old, new):
    old_path = MONK / old
    new_path = MONK / new
    if old_path.exists() and old_path != new_path:
        if new_path.exists():
            old_path.unlink()
        else:
            old_path.rename(new_path)


def extract_overall(path):
    text = path.read_text(encoding="utf-8")
    rows = []
    in_section = False
    for line in text.splitlines():
        if line.strip() == "## 综合评分":
            in_section = True
            continue
        if in_section and line.startswith("## "):
            break
        if in_section and line.startswith("| Tier"):
            parts = [p.strip() for p in line.strip("|").split("|")]
            if len(parts) >= 3:
                rows.append((parts[0], parts[1], parts[2]))
    return rows


def bucket(files):
    data = {tier: {} for tier in TIERS}
    for name in files:
        path = MONK / f"{name}.md"
        if not path.exists():
            continue
        for tier, rank, reason in extract_overall(path):
            data.setdefault(tier, {}).setdefault(rank, []).append((name, reason))
    return data


def render_leaderboard(title, intro, files):
    order = ["S+", "S", "S-", "A+", "A", "A-", "B", "C", "D", "E", "E-", "F"]
    data = bucket(files)
    out = [f"## {title}", ""]
    if intro:
        out.extend([intro, ""])
    for tier in TIERS:
        out.append(f"{tier}:")
        any_rank = False
        for rank in order:
            entries = data.get(tier, {}).get(rank, [])
            if not entries:
                continue
            any_rank = True
            out.append(f"- {rank}:")
            for name, reason in entries:
                out.append(f"  - {name}：{reason}")
        if not any_rank:
            out.append("- 无")
        out.append("")
    return "\n".join(out).rstrip()


