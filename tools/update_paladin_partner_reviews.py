from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
PAL = ROOT / "Rankings" / "圣武士"


def write(name: str, text: str) -> None:
    PAL.mkdir(parents=True, exist_ok=True)
    (PAL / name).write_text(text.strip() + "\n", encoding="utf-8")


def rename_if_needed(old: str, new: str) -> None:
    old_path = PAL / old
    new_path = PAL / new
    if old_path.exists() and not new_path.exists():
        old_path.rename(new_path)


def item_table(rows):
    headers = ["项目", "Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]
    out = ["| " + " | ".join(headers) + " |", "|---|---|---|---|---|"]
    for row in rows:
        out.append("| " + " | ".join(row) + " |")
    return "\n".join(out)


