TIERS = ["Tier 1（1-4）", "Tier 2（5-10）", "Tier 3（11-16）", "Tier 4（17-20）"]


def replace_section(text: str, heading: str, body: str) -> str:
    pattern = rf"({re.escape(heading)}\n)(.*?)(?=\n## |\Z)"
    new_text, count = re.subn(pattern, lambda m: m.group(1) + body.rstrip() + "\n", text, flags=re.S)
    if count != 1:
        raise RuntimeError(f"failed replacing section {heading!r}")
    return new_text


def replace_position(text: str, body: str) -> str:
    if "## 队伍定位摘要\n" in text:
        return replace_section(text, "## 队伍定位摘要", body)
    if "## 队伍定位调整\n" in text:
        return replace_section(text, "## 队伍定位调整", body)
    raise RuntimeError("missing position section")


def overall_table(rows: list[tuple[str, str]]) -> str:
    out = ["| 阶段 | 综合强度 | 具体理由 |", "|---|---|---|"]
    for tier, (rank, reason) in zip(TIERS, rows):
        out.append(f"| {tier} | {rank} | {reason} |")
    return "\n".join(out)


def update_file(name: str, position: str, detail: str, overall: list[tuple[str, str]]) -> None:
    path = WARLOCK / name
    text = path.read_text(encoding="utf-8")
    text = replace_position(text, position)
    text = replace_section(text, "## 详细评价", detail)
    text = replace_section(text, "## 综合评分", overall_table(overall))
    path.write_text(text, encoding="utf-8")


def replace_aspect_row(text: str, aspect: str, values: list[str]) -> str:
    replacement = f"| {aspect} | {' | '.join(values)} |"
    new_text, count = re.subn(rf"^\| {re.escape(aspect)} \|.*\|$", replacement, text, flags=re.M)
    if count != 1:
        raise RuntimeError(f"failed replacing aspect row {aspect!r}")
    return new_text


def update_aspects(name: str, rows: dict[str, list[str]]) -> None:
    path = WARLOCK / name
    text = path.read_text(encoding="utf-8")
    for aspect, values in rows.items():
        text = replace_aspect_row(text, aspect, values)
    path.write_text(text, encoding="utf-8")


