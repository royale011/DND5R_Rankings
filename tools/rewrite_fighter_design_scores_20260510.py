def write_utf8_lf(path: Path, text: str) -> None:
    path.write_bytes((text.rstrip() + "\n").encode("utf-8"))


def table(rows: list[tuple[str, str, str]]) -> str:
    out = ["| 设计项 | 评分 | 具体理由 |", "|---|---|---|"]
    out.extend(f"| {item} | {score} | {reason} |" for item, score, reason in rows)
    return "\n".join(out)


def replace_design(path: Path, rows: list[tuple[str, str, str]]) -> None:
    text = path.read_text(encoding="utf-8")
    pattern = re.compile(r"## 设计相关评分\n.*?(?=\n## |\Z)", re.S)
    if not pattern.search(text):
        raise ValueError(f"{path} missing design section")
    text = pattern.sub("## 设计相关评分\n" + table(rows) + "\n", text, count=1)
    write_utf8_lf(path, text)


